//C_swap
//Program to swap two numbers 
class C_swap{
	public static void main(String args[]){
		int x = 5, y=10;
		int temp;
		System.out.println("Before swap : ");
		System.out.println("x = "+x+"   "+"y = "+y);
		temp = x;
		x = y;
		y = temp;
		System.out.println("Before swap : ");
		System.out.println("x = "+x+"   "+"y = "+y);

	}
}